namespace Models
{
    /// <summary>
    /// Represents the types of celestial bodies.
    /// </summary>
    public enum CelestialBodyType
    {
        /// <summary>
        /// Represents a sun, which is a star at the center of a solar system.
        /// </summary>
        Sun,

        /// <summary>
        /// Represents a planet, which is a large celestial body that orbits a star.
        /// </summary>
        Planet,

        /// <summary>
        /// Represents a moon, which is a natural satellite that orbits a planet.
        /// </summary>
        Moon
    }
}